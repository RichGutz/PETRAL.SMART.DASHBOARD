    # A. Snap grid points near the perimeter to the perimeter line.
    # B. Generate explicit grid triangles (i,j,k).
    # C. Filter triangles based on whether they are Inside or Outside the cut.

    # --- 3.5 STATISTICS (VOLUME) ---
    cell_area = GRID_SPACING_METERS * GRID_SPACING_METERS
    total_cut_volume = 0
    points_cut = 0
    
