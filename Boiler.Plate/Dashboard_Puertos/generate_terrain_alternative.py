def point_in_triangle(pt, v1, v2, v3):
    def sign(p1, p2, p3):
        return (p1[0] - p3[0]) * (p2[1] - p3[1]) - (p2[0] - p3[0]) * (p1[1] - p3[1])
    
    d1 = sign(pt, v1, v2)
    d2 = sign(pt, v2, v3)
    d3 = sign(pt, v3, v1)
    
    has_neg = (d1 < 0) or (d2 < 0) or (d3 < 0)
    has_pos = (d1 > 0) or (d2 > 0) or (d3 > 0)
    
    return not (has_neg and has_pos)

def get_elevation_batch_open_meteo(locations):
    """
    Fetch elevations from Open-Meteo for a list of (lon, lat).
    NOTE: Input locations here are [lon, lat], but API expects lat,lon.
    """
    base_url = "https://api.open-meteo.com/v1/elevation"
    
    # Process in chunks to respect URL length / API limits
    # Smaller chunks for repair to be agile
    
    # Just handle one batch call logic here, caller handles looping
    # Actually, to fit the existing signature flow, let's process the input list fully
    
    lats = [f"{p[1]:.6f}" for p in locations]
    lons = [f"{p[0]:.6f}" for p in locations]
    
    url = f"{base_url}?latitude={','.join(lats)}&longitude={','.join(lons)}"
    
    try:
        resp = requests.get(url, timeout=10)
        data = resp.json()
        
        if 'elevation' in data:
            return data['elevation']
        elif 'error' in data:
            print(f"    (Open-Meteo Error: {data.get('reason')})")
            return None # Explicit failure
        else:
            return None
            
    except Exception as e:
        print(f"    (Open-Meteo Request Failed: {e})")
        return None

def get_elevation_batch_open_elevation(locations):

    """
    Fetches elevation from Open-Elevation API (POST request).
    locations: list of [lon, lat]
    """
    # Format for Open-Elevation: {"locations": [{"latitude": 10, "longitude": 10}, ...]}
    payload = {
        "locations": [{"latitude": p[1], "longitude": p[0]} for p in locations]
    }
    
    url = "https://api.open-elevation.com/api/v1/lookup"
    
    for attempt in range(3):
        try:
            resp = requests.post(url, json=payload, headers={'Content-Type': 'application/json'}, timeout=30)
            
            if resp.status_code == 200:
                data = resp.json()
                # Extract results in order
                results = data.get('results', [])
                elevations = [r['elevation'] for r in results]
                
                if len(elevations) != len(locations):
                    print(f"  ⚠ Mismatch: Sent {len(locations)}, got {len(elevations)}")
                    return [0] * len(locations) # Fail safe
                
                return elevations
            else:
                print(f"  HTTP Error {resp.status_code}: {resp.text[:100]}")
                time.sleep(2)
        
        except Exception as e:
            print(f"  Request failed (Attempt {attempt+1}): {e}")
            time.sleep(2)
            
    return [0] * len(locations) # Return 0s if all retries fail

