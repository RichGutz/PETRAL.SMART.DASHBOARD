    def get_z_at_geo(lon, lat, grid_pts, z_vals, grid_w, grid_h, min_x, max_y, dx, dy):
        # Bilinear Interpolation
        # Grid loop: curr_lat start at max_lat, goes down (-dy)
        #            curr_lon start at min_lon, goes right (+dx)
        
        # Row (y): distance from max_lat
        r_f = (max_y - lat) / dy
        c_f = (lon - min_x) / dx
        
        r0 = int(r_f)
        c0 = int(c_f)
        
        # Clamp
        if r0 < 0: r0 = 0
        if r0 >= grid_h - 1: r0 = grid_h - 2
        if c0 < 0: c0 = 0
        if c0 >= grid_w - 1: c0 = grid_w - 2
        
        # Indices
        # idx = r * width + c
        i00 = r0 * grid_w + c0
        i01 = r0 * grid_w + (c0 + 1)
        i10 = (r0 + 1) * grid_w + c0
        i11 = (r0 + 1) * grid_w + (c0 + 1)
        
        if i11 >= len(z_vals): return 0
        
        z00 = z_vals[i00]
        z01 = z_vals[i01]
        z10 = z_vals[i10]
        z11 = z_vals[i11]
        
        # Local coords
        tr = r_f - r0
        tc = c_f - c0
        
        # Interpolate
        top = z00 * (1 - tc) + z01 * tc
        bot = z10 * (1 - tc) + z11 * tc
        return top * (1 - tr) + bot * tr

    def generate_hex_wall_3d(p_start, p_end, z_floor, z_start_ceil, z_end_ceil, u_global_offset=0.0):
        # Generate 3D line segments for a hex mesh on this vertical plane
        
        # 1. Plane UV basis
        # U: horizontal along wall (using u_global_offset to keep phase alignment)
        # V: vertical (Z)
        
        dist = math.sqrt((p_end[0]-p_start[0])**2 + (p_end[1]-p_start[1])**2) 
        # Convert dist to meters approx for hex sizing
        # lat avg
        lat_avg = (p_start[1] + p_end[1]) / 2
        deg_len = math.sqrt(((p_end[0]-p_start[0]) * 111320 * math.cos(math.radians(lat_avg)))**2 + ((p_end[1]-p_start[1])*111132)**2)
        
        if deg_len < 0.1: return [], [], []
        
        HEX_SIZE = 15.0 # meters
        
        u_len = deg_len
        v_start = z_start_ceil - z_floor
        v_end = z_end_ceil - z_floor
        max_v = max(v_start, v_end)
        
        lines_x = []
        lines_y = []
        lines_z = []
        
        # Hex Grid Logic (Honeycomb)
        w = math.sqrt(3) * HEX_SIZE # ~26m
        h = 2 * HEX_SIZE # 30m
        vert_dist = 0.75 * h # 22.5m
        
        # Grid range relative to GLOBAL offset
        # We need to cover the local range [u_global_offset, u_global_offset + u_len]
        
        start_u = u_global_offset
        end_u = u_global_offset + u_len
        
        # Determine cols range
        start_col = int(start_u / w) - 1
        end_col = int(end_u / w) + 2
        
        rows = int(max_v / vert_dist) + 1
        
        def get_geo(u_local, v):
            # Transform local (u_local, v) back to (lon, lat, z)
            # U_local is from 0 to u_len
            frac = u_local / u_len
            
            lon = p_start[0] + frac * (p_end[0] - p_start[0])
            lat = p_start[1] + frac * (p_end[1] - p_start[1])
            z = z_floor + v
            return lon, lat, z

        # Generate Hex edges
        for r in range(rows):
            yr = r * vert_dist
            offset_x = (w / 2.0) if (r % 2 == 1) else 0.0
            
            # Iterate cols based on global position
            for c in range(start_col, end_col):
                xc_global = c * w + offset_x
                
                # Convert to local U
                xc_local = xc_global - u_global_offset
                
                # Check rough bounds (optimization)
                if xc_local < -HEX_SIZE or xc_local > u_len + HEX_SIZE: continue
                
                # Points of a hex (Pointy Topped)
                pts = []
                for k in range(7):
                    ang = math.radians(30 + 60 * k)
                    px = xc_local + HEX_SIZE * math.cos(ang)
                    py = yr + HEX_SIZE * math.sin(ang)
                    pts.append((px, py))
                
                # Clip and Add lines
                for k in range(6):
                    p1 = pts[k]
                    p2 = pts[k+1]
                    
                    # 1. Horizontal Clipping (Local Segment)
                    # We only care about the part of the line INSIDE [0, u_len]
                    # Simple accept/reject is risky for continuity, but improved:
                    
                    # If both outside same side, skip
                    if (p1[0] < 0 and p2[0] < 0) or (p1[0] > u_len and p2[0] > u_len): continue
                    
                    # Clamp X to segment
                    x1_c = max(0, min(u_len, p1[0]))
                    x2_c = max(0, min(u_len, p2[0]))
                    
                    # If clamped interval is tiny, skip
                    if abs(x2_c - x1_c) < 0.01: continue
                    
                    # Interpolate Y (height) at clamped X
                    # Line eq: y = m*x + b
                    if abs(p2[0] - p1[0]) > 0.001:
                        m = (p2[1] - p1[1]) / (p2[0] - p1[0])
                        y1_c = p1[1] + m * (x1_c - p1[0])
                        y2_c = p2[1] + m * (x2_c - p1[0])
                    else:
                        y1_c = p1[1]
                        y2_c = p2[1]
                    
                    # 2. Vertical Clipping (Ceiling) aka Clamping
                    # Ceiling func
                    def get_ceil(u):
                        return (z_start_ceil * (1 - u/u_len) + z_end_ceil * (u/u_len)) - z_floor
                    
                    ceil1 = get_ceil(x1_c)
                    ceil2 = get_ceil(x2_c)
                    
                    # If completely above ceiling, skip
                    if y1_c > ceil1 and y2_c > ceil2: continue
                    # If completely below floor, skip
                    if y1_c < 0 and y2_c < 0: continue
                    
                    # Clamp Y to ceiling/floor
                    y1_final = max(0, min(ceil1, y1_c))
                    y2_final = max(0, min(ceil2, y2_c))
                    
                    # Generate 3D Coords
                    g1 = get_geo(x1_c, y1_final)
                    g2 = get_geo(x2_c, y2_final)
                    
                    lines_x.extend([g1[0], g2[0], None])
                    lines_y.extend([g1[1], g2[1], None])
                    lines_z.extend([g1[2], g2[2], None])
                    
        return lines_x, lines_y, lines_z

